#include <bits/stdc++.h>

using namespace std;

int main() {

    int age;
    double salary;
    char gender;
    string name;

    age = 20;
    salary = 5800.5;
    gender = 'F';
    name = "Maria Silva";

    cout << fixed << setprecision(2);
    cout << "Age = " << age << endl;
    cout << "Salary = " << salary << endl;
    cout << "Gender = " << gender << endl;
    cout << "Name = " << name << endl;

    return 0;
}
